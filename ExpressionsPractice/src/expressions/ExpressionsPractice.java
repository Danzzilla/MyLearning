package expressions;

public class ExpressionsPractice {

	public static void main(String[] args){
	   int exp1 = 3-1;                            //calling variable exp1 to be type int and assigning it a value (in this case 3-1)               
	   System.out.println(exp1);                  //prints the calculated value of exp1                                                  
	   System.out.println();                      //empty line printed to serve as a space between answer to expression 1 and 2              
	                                                                                                                    
	   int exp2 = 2+2+3+4;                                                                                           
	   System.out.println(exp2);                                                                                     
	   System.out.println();                                                                                     
	                                                                                                                
	   double exp3 = 2+2+3.1+4;                   //calling and assigning variable exp3 to be type double                               
	   System.out.println(exp3);
	   System.out.println();
	   
	   String exp4 = 2+" 2+3 "+4;                 //calling and assigning variable exp4 to be a string
	   System.out.println(exp4);
	   System.out.println();
	   
	   String exp5 = 3+4+" 2+2";
	   System.out.println(exp5);
	   System.out.println();
	   
	   int exp6 = 2+2+(3/4);
	   System.out.println(exp6);
	   System.out.println();
	   
	   double exp7 = (12/2)+(13/4.0);
	   System.out.println(exp7);
	   System.out.println();
	   
	   String exp8 = "hello" +2*4;
	   System.out.println(exp8);
	   System.out.println();
	   
	   double exp9 = 2+2.0+2*2+2;
	   System.out.println(exp9);
	   System.out.println();
	   
	   double exp10 = 4+1+9+.01+(-3+10)+11/3;
	   System.out.println(exp10);
	   System.out.println();
	   
	   double exp11 = 8+16/5+4+0+12/5.0;
	   System.out.println(exp11);
	   System.out.println();
	   
	   int exp12 = 1+1+(8-2)*1+1;
	   System.out.println(exp12);
	   System.out.println();
	   
	   String exp13 = 5+2+"(1+1)"+4+2*3;
	   System.out.println(exp13);
	   System.out.println();
	   
	   String exp14 = "1"+2+3+"4"+5*6+"7"+(8+9);
	   System.out.println(exp14);
	   System.out.println();
	   }                                          //end of main function
}                                                 //end of program
 
